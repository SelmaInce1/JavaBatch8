package com.syntax.class07;

public class IncrementandDecrement {

	public static void main(String[] args) {
		
		int num=90;
		num=num+1;
		num+=1;
		
		System.out.println(num);
		
		num++; ///For just adding 1
		System.out.println(num);
		
		int num1=100;
		num1-=1;
		
		num1--; ///For just substracting 1
		System.out.println(num1);
		
		System.out.println("---------------------------");
		
		///Loops in java
		//while, do while,for
		
		

	}

}
